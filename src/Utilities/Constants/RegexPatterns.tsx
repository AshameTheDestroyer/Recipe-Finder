const REGEX_PATTERNS = {
    username: [
        "(",
        "[a-zA-Z]{1}",
        "[a-zA-Z0-9]*",
        "[\._\-]{0,1}",
        "[a-zA-Z0-9]*",
        ")*",
        "[a-zA-Z0-9]{1}",
    ].join(""),

    mail: [
        "(",
        "[a-zA-Z]{1}",
        "[a-zA-Z0-9]*",
        "[\._\-]{0,1}",
        "[a-zA-Z0-9]*",
        ")*",
        "[a-zA-Z0-9]{1}",
        "[@]{1}",
        "(",
        "[a-zA-Z]{1}",
        "[a-zA-Z0-9]*",
        "[_\-]{0,1}",
        "[a-zA-Z0-9]*",
        ")*",
        "[a-zA-Z0-9]{1}",
        "[\.]{1}",
        "([a-zA-Z]{2,})",
    ].join(""),

    password: [
        "^(?=.*[a-z])",
        "(?=.*[A-Z])",
        "(?=.*[0-9])",
        "(?=.*[!@#$%^&*_=+-])",
        ".{8,20}$",
    ].join(""),
};

export default REGEX_PATTERNS;